void bootloader_uart_data_read(void) {
	uint8_t bl_rx_length = 0;

	while (1) {
		memset(bootloader_rx_data, 0, BL_RX_DATA_LENGTH);

		HAL_UART_Receive(&huart2, bootloader_rx_data, 1, HAL_MAX_DELAY);

		bl_rx_length = bootloader_rx_data[0];

		HAL_UART_Receive(&huart2, &bootloader_rx_data[1], bl_rx_length, HAL_MAX_DELAY);

		switch (bootloader_rx_data[1]) {
		case BL_GET_VER:/*CALISIYOR CRC düzeltmesi yapıldı*/
			bootloader_get_ver_cmd(bootloader_rx_data); //calisiyor
			break;
		case BL_GET_HELP:/*CALISIYOR CRC düzeltmesi yapıldı test edilecek */
			bootloader_get_help_cmd(bootloader_rx_data); //calıisiyor
			break;
		case BL_GET_CID:/*CALISIYOR*/
			bootloader_get_cid_cmd(bootloader_rx_data); //calıisiyor
			break;
		case BL_GET_RDP_STATUS:/*CALISIYOR*/
			bootloader_get_rdp_cmd(bootloader_rx_data);// calısiyor
			break;
		case BL_GO_TO_ADDR:/*CALISIYOR*/
			bootloader_go_to_addr_cmd(bootloader_rx_data);// calısiyor
			break;
			/*	Once page sonra ondan sonraki kaç tane page silincek ise o girilecek  */
		case BL_FLASH_ERASE:/*CALISIYOR*/
			bootloader_flash_erase_cmd(bootloader_rx_data);
			break;
			/*	Yazma yapılmadan önce kullanılacak pageler flash erase yapılacak*/
		case BL_MEM_WRITE:/*CALISIYOR*/
			bootloader_mem_write_cmd(bootloader_rx_data);
			break;
		case BL_EN_RW_PROTECT:
			bootloader_enable_read_write_protect_cmd(bootloader_rx_data);
			break;
		case BL_DIS_R_W_PROTECT:
			bootloader_disable_read_write_protect_cmd(bootloader_rx_data);
		default:
			break;
		}
	}
}

void bootloader_jump_to_user_application(void) {
	//  Kesmeleri kapa
	/*__disable_irq();// __disable_irq deyince hal_delay çalışmıyor!!!!!*/

	printMessage("bootloader_jump_to_user_application() \n");

	//  MSP'nin değerini tut
	uint32_t mspValue = *(volatile uint32_t*) FLASH_SECTOR2_BASE_ADDRESS;
	printMessage("MSP Value: %#x \n", mspValue);

	// Sıfırlama işleyicisinin değerini tut
	uint32_t resetValue = *(volatile uint32_t*) (FLASH_SECTOR2_BASE_ADDRESS + 4);
	printMessage("Reset Value: %#x \n", resetValue);

	//  Periferleri sıfırla ve devre dışı bırak

	SCB->VTOR = FLASH_SECTOR2_BASE_ADDRESS;
	//__set_MSP(mspValue);	// Bu fonksiyon F407 De calisiyordu ama
	//L053 de çalışmıyor
	SysTick->CTRL = 0;
	SysTick->LOAD = 0;
	SysTick->VAL = 0;
	HAL_I2C_DeInit(&hi2c1);
	HAL_UART_MspDeInit(&huart2);
	HAL_GPIO_DeInit(GPIOC, GPIO_PIN_13);

	HAL_CRC_DeInit(&hcrc);
	HAL_RCC_DeInit();

	HAL_DeInit();
	uint32_t go_address =
			*((volatile uint32_t*) (FLASH_SECTOR2_BASE_ADDRESS + 4));
	void (*jump_to_app)(void) = (void *)go_address;
	jump_to_app();

}
